package com.intertek.phoenix.externalEntity;

import java.util.Date;

/**
 * Object to send to ESB
 * 
 * @author Eric.Nguyen
 */
public class CreditHoldX {
    private String customerId;
    private String customerName;
    private String creditHoldFlag; //"Y" means true anything else is false
    private Date dateTime;
    private String operatorId;

    public String getCustomerId() {
        return customerId;
    }

    public void setCustomerId(String customerId) {
        this.customerId = customerId;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public Date getDateTime() {
        return dateTime;
    }

    public void setDateTime(Date dateTime) {
        this.dateTime = dateTime;
    }

    public String getOperatorId() {
        return operatorId;
    }

    public void setOperatorId(String operatorId) {
        this.operatorId = operatorId;
    }

    public String getCreditHoldFlag() {
        return creditHoldFlag;
    }

    public void setCreditHoldFlag(String creditHoldFlag) {
        this.creditHoldFlag = creditHoldFlag;
    }

}
