package com.intertek.phoenix.externalEntity;

import com.intertek.phoenix.entity.Employee;

/**
 * @author Eric.Nguyen
 */
public class EmployeeX {
    private String employeeId;
    private String firstName;
    private String lastName;
    private String samAccount;
    private String businessUnit;
    private String operatingUnit;

    public EmployeeX(){
        
    }
    
    public Employee convert(){
        Employee em=new Employee();
        em.setEmployeeId(getEmployeeId());
        em.setFirstName(getFirstName());
        em.setLastName(getLastName());
        em.setSamAccount(getSamAccount());
        em.setBusinessUnit(getBusinessUnit());
        em.setOperatingUnit(getOperatingUnit());
        return em;
    }
    
    public String getEmployeeId() {
        return employeeId;
    }

    public void setEmployeeId(String employeeId) {
        this.employeeId = employeeId;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getSamAccount() {
        return samAccount;
    }

    public void setSamAccount(String samAccount) {
        this.samAccount = samAccount;
    }

    public String getBusinessUnit() {
        return businessUnit;
    }

    public void setBusinessUnit(String businessUnit) {
        this.businessUnit = businessUnit;
    }

    public String getOperatingUnit() {
        return operatingUnit;
    }

    public void setOperatingUnit(String operatingUnit) {
        this.operatingUnit = operatingUnit;
    }
}
