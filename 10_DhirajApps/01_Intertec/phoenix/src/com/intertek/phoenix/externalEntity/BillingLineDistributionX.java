package com.intertek.phoenix.externalEntity;

import com.intertek.phoenix.entity.BillingLineDistribution;

/**
 * @author Eric.Nguyen
 */
public class BillingLineDistributionX {
    private String businessUnit;
    private String invoiceNumber;
    private int lineSeqNumber;
    private int lineDistSeqNumber;
    private String jobNumber;
    private String account;
    private String warehouseId;
    private String departmentId;
    private String businessStream;
    private String serviceOffering;
    private double distributionAmt;

    public BillingLineDistribution convert(){
        BillingLineDistribution bd=new BillingLineDistribution();
        bd.setBusinessUnit(this.getBusinessUnit());
        bd.setInvoiceNumber(this.getInvoiceNumber());
        bd.setLineSeqNumber(this.getLineSeqNumber());
        bd.setLineDistSeqNumber(this.getLineDistSeqNumber());
        bd.setJobNumber(this.getJobNumber());
        bd.setAccountCode(this.getAccount());
        bd.setWarehouseId(this.getWarehouseId());
        bd.setDepartmentId(this.getDepartmentId());
        bd.setBusinessStream(this.getBusinessStream());
        bd.setServiceOffering(this.getServiceOffering());
        bd.setDistributionAmt(this.getDistributionAmt());
        return bd;
    }
    
    public String getBusinessUnit() {
        return businessUnit;
    }

    public void setBusinessUnit(String businessUnit) {
        this.businessUnit = businessUnit;
    }

    public String getInvoiceNumber() {
        return invoiceNumber;
    }

    public void setInvoiceNumber(String invoiceNumber) {
        this.invoiceNumber = invoiceNumber;
    }

    public int getLineSeqNumber() {
        return lineSeqNumber;
    }

    public void setLineSeqNumber(int lineSeqNumber) {
        this.lineSeqNumber = lineSeqNumber;
    }

    public int getLineDistSeqNumber() {
        return lineDistSeqNumber;
    }

    public void setLineDistSeqNumber(int lineDistSeqNumber) {
        this.lineDistSeqNumber = lineDistSeqNumber;
    }

    public String getJobNumber() {
        return jobNumber;
    }

    public void setJobNumber(String jobNumber) {
        this.jobNumber = jobNumber;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getWarehouseId() {
        return warehouseId;
    }

    public void setWarehouseId(String warehouseId) {
        this.warehouseId = warehouseId;
    }

    public String getDepartmentId() {
        return departmentId;
    }

    public void setDepartmentId(String departmentId) {
        this.departmentId = departmentId;
    }

    public String getBusinessStream() {
        return businessStream;
    }

    public void setBusinessStream(String businessStream) {
        this.businessStream = businessStream;
    }

    public String getServiceOffering() {
        return serviceOffering;
    }

    public void setServiceOffering(String serviceOffering) {
        this.serviceOffering = serviceOffering;
    }

    public double getDistributionAmt() {
        return distributionAmt;
    }

    public void setDistributionAmt(double distributionAmt) {
        this.distributionAmt = distributionAmt;
    }
}
