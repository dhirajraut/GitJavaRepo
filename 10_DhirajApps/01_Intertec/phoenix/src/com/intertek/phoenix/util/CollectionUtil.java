package com.intertek.phoenix.util;

import java.util.ArrayList;
import java.util.List;

/**
 * 
 * @author richard.qin
 */
public class CollectionUtil {
    
    public static <T> List<T> arrayToList(T... objs){
        List<T> list = new ArrayList<T>();
        for(T t: objs){
            list.add(t);
        }
        return list;
    }
}
