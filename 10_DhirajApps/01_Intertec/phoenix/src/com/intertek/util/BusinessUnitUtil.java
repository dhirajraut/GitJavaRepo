package com.intertek.util;

import com.intertek.entity.BusinessUnit;

public class BusinessUnitUtil
{
  public static void copyBusinessUnitAttributes(BusinessUnit from, BusinessUnit to)
  {
    if( (from == null) || (to == null)) return;

    if(from.getStatus() != null) to.setStatus(from.getStatus());
    if(from.getCurrencyBase() != null) to.setCurrencyBase(from.getCurrencyBase());
    if(from.getDescription() != null) to.setDescription(from.getDescription());

    if(from.getShortDescription() != null) to.setShortDescription(from.getShortDescription());
    if(from.getDefaultBuInd() != null) to.setDefaultBuInd(from.getDefaultBuInd());
    if(from.getVatEnabledInd() != null) to.setVatEnabledInd(from.getVatEnabledInd());
    if(from.getOrgName() != null) to.setOrgName(from.getOrgName());
    if(from.getAddress1() != null) to.setAddress1(from.getAddress1());
    if(from.getAddress2() != null) to.setAddress2(from.getAddress2());
    if(from.getAddress3() != null) to.setAddress3(from.getAddress3());
    if(from.getAddress4() != null) to.setAddress4(from.getAddress4());

    if(from.getCity() != null) to.setCity(from.getCity());
    if(from.getStateCode() != null) to.setStateCode(from.getStateCode());
    if(from.getCountryCode() != null) to.setCountryCode(from.getCountryCode());

    if(from.getNum1() != null) to.setNum1(from.getNum1());
    if(from.getNum2() != null) to.setNum2(from.getNum2());

    if(from.getHouseType() != null) to.setHouseType(from.getHouseType());
    if(from.getAddrField1() != null) to.setAddrField1(from.getAddrField1());
    if(from.getAddrField2() != null) to.setAddrField2(from.getAddrField2());
    if(from.getAddrField3() != null) to.setAddrField3(from.getAddrField3());

    if(from.getCounty() != null) to.setCounty(from.getCounty());
    if(from.getPostal() != null) to.setPostal(from.getPostal());

    if(from.getPhonePrefix() != null) to.setPhonePrefix(from.getPhonePrefix());

    if(from.getPhoneNumber() != null) to.setPhoneNumber(from.getPhoneNumber());
    if(from.getPhoneExtension() != null) to.setPhoneExtension(from.getPhoneExtension());
    if(from.getFax() != null) to.setFax(from.getFax());
  }
}
