package com.intertek.domain;

import java.util.ArrayList;
import java.util.List;

public class BaseSelects
{
  protected boolean[] selects;
  protected List results = new ArrayList();

  protected List deletedResults = new ArrayList();
  protected List addedResults = new ArrayList();

  protected List deletedControls = new ArrayList();

  public void setSelects(boolean[] selects)
  {
    this.selects = selects;
  }

  public boolean[] getSelects()
  {
    return selects;
  }


  public void setResults(List results)
  {
    this.results = results;
  }

  public List getResults()
  {
    return results;
  }

  public void setDeletedResults(List deletedResults)
  {
    this.deletedResults = deletedResults;
  }

  public List getDeletedResults()
  {
    return deletedResults;
  }

  public void setAddedResults(List addedResults)
  {
    this.addedResults = addedResults;
  }

  public List getAddedResults()
  {
    return addedResults;
  }


  public void setDeletedControls(List deletedControls)
  {
    this.deletedControls = deletedControls;
  }

  public List getDeletedControls()
  {
    return deletedControls;
  }
}

