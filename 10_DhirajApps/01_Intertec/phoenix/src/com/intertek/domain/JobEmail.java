package com.intertek.domain;

import org.springmodules.validation.bean.conf.loader.annotation.handler.CascadeValidation;

public class JobEmail
{
  @CascadeValidation

 

  private AddJobOrder[] addJobOrders;
 
  
  private String to;
  private String cc;
  private String subject;
  private String coordinator;
  private String introduction;
  private String introductionToDisplay;
  private String custCode;
  private long contactId;
  private boolean emailSentFlag = false;


public String getCc() {
	return cc;
}

public void setCc(String cc) {
	this.cc = cc;
}

public String getCoordinator() {
	return coordinator;
}

public void setCoordinator(String coordinator) {
	this.coordinator = coordinator;
}

public String getIntroduction() {
	return introduction;
}

public void setIntroduction(String introduction) {
	this.introduction = introduction;
}

public String getSubject() {
	return subject;
}

public void setSubject(String subject) {
	this.subject = subject;
}

public String getTo() {
	return to;
}

public void setTo(String to) {
	this.to = to;
}



public AddJobOrder[] getAddJobOrders() {
	return addJobOrders;
}

public void setAddJobOrders(AddJobOrder[] addJobOrders) {
	this.addJobOrders = addJobOrders;
}

public String getCustCode() {
	return custCode;
}

public void setCustCode(String custCode) {
	this.custCode = custCode;
}

public long getContactId() {
	return contactId;
}

public void setContactId(long contactId) {
	this.contactId = contactId;
}

public boolean isEmailSentFlag() {
	return emailSentFlag;
}

public void setEmailSentFlag(boolean emailSentFlag) {
	this.emailSentFlag = emailSentFlag;
}

/**
 * @return the introductionToDisplay
 */
public String getIntroductionToDisplay() {
	return introductionToDisplay;
}

/**
 * @param introductionToDisplay the introductionToDisplay to set
 */
public void setIntroductionToDisplay(String introductionToDisplay) {
	this.introductionToDisplay = introductionToDisplay;
}




  
  

  
  
  


}
