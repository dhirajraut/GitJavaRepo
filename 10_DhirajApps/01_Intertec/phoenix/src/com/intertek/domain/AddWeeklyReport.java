package com.intertek.domain;

public class AddWeeklyReport
{
	  private StringSearchField branch;
	  private StringSearchField businessUnit;
	  private DateSearchField asOfDate;
	  private String dateFormat;
	  private StringSearchField currency;
 
  public AddWeeklyReport()
  {
	  branch = new StringSearchField();
	  businessUnit = new StringSearchField();
	  asOfDate = new DateSearchField();
	  currency = new StringSearchField();
  }



public StringSearchField getBranch() {
	return branch;
}



public void setBranch(StringSearchField branch) {
	this.branch = branch;
}

public StringSearchField getBusinessUnit() {
	return businessUnit;
}

public void setBusinessUnit(StringSearchField businessUnit) {
	this.businessUnit = businessUnit;
}

public DateSearchField getAsOfDate() {
	return asOfDate;
}

public void setAsOfDate(DateSearchField asOfDate) {
	this.asOfDate = asOfDate;
}

public String getDateFormat() {
	return dateFormat;
}

public void setDateFormat(String dateFormat) {
	this.dateFormat = dateFormat;
}

public StringSearchField getCurrency() {
	return currency;
}

public void setCurrency(StringSearchField currency) {
	this.currency = currency;
}

}
