package com.intertek.pagination;

/**
 * Represent a page in jsp used by Pagination class.
 **/

public class Page
{
  private String name;
  private int pageNumber;
  private boolean selected;

  /**
   * .ctr
   *
   * @param name - name of the page.
   **/
  public Page(String name)
  {
    this.name = name;
  }

  /**
   * Get name of the page.
   *
   * @return - the name of the page.
   **/
  public String getName()
  {
    return name;
  }

  /**
   * Set the name of the page.
   *
   * @param name - the name of the page.
   **/
  public void setName(String name)
  {
    this.name = name;
  }

  /**
   * Get the page number of the page.
   *
   * @return - the page number of the page.
   **/
  public int getPageNumber()
  {
    return pageNumber;
  }

  /**
   * Set the page number of the page.
   *
   * @param pageNumber - the page number of the page.
   **/
  public void setPageNumber(int pageNumber)
  {
    this.pageNumber = pageNumber;
  }

  /**
   * Get the selected flag of the page.
   *
   * @return - the selected flag of the page.
   **/
  public boolean isSelected()
  {
    return selected;
  }

  /**
   * Set the selected flag of the page.
   *
   * @param selected - the selected flag of the page.
   **/
  public void setSelected(boolean selected)
  {
    this.selected = selected;
  }
}
